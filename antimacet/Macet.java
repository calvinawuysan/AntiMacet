package id.kharisma.studio.antimacet;

public class Macet {
    String reporter2;
    String wilayah2;
    String penjelasan2;

    public Macet(String reporter2, String wilayah2, String penjelasan2) {
        this.reporter2 = reporter2;
        this.wilayah2 = wilayah2;
        this.penjelasan2 = penjelasan2;
    }

    public String getReporter2() {
        return reporter2;
    }

    public String getWilayah2() {
        return wilayah2;
    }

    public String getPenjelasan2() {
        return penjelasan2;
    }
}
