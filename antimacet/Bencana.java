package id.kharisma.studio.antimacet;

public class Bencana {
    String reporter1;
    String wilayah1;
    String penjelasan1;

    public Bencana(String reporter1, String wilayah1, String penjelasan1) {
        this.reporter1 = reporter1;
        this.wilayah1 = wilayah1;
        this.penjelasan1 = penjelasan1;
    }

    public String getReporter1() {
        return reporter1;
    }

    public String getWilayah1() {
        return wilayah1;
    }

    public String getPenjelasan1() {
        return penjelasan1;
    }
}
