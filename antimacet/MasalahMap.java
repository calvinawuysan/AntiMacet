package id.kharisma.studio.antimacet;

public class MasalahMap {
    String reporter3;
    String penjelasan3;

    public MasalahMap(String reporter3, String penjelasan3) {
        this.reporter3 = reporter3;
        this.penjelasan3 = penjelasan3;
    }

    public String getReporter3() {
        return reporter3;
    }

    public String getPenjelasan3() {
        return penjelasan3;
    }
}
