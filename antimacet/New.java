package id.kharisma.studio.antimacet;

public class New {
    String judul, tanggal, penjelasan;

    public New(String judul, String tanggal, String penjelasan) {
        this.judul = judul;
        this.tanggal = tanggal;
        this.penjelasan = penjelasan;
    }

    public String getJudul() {
        return judul;
    }

    public String getTanggal() {
        return tanggal;
    }

    public String getPenjelasan() {
        return penjelasan;
    }
}


